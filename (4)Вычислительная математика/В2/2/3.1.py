import numpy as np

ORIGIN = 1

A = np.array([
    [ 0.010,  0.008,  0.200,  0.050 ],
    [ -0.088,  0.000,  0.013,  0.050 ],
    [ 0.250,  0.067,  0.067,  0.060 ],
    [ 0.0057,  0.150, -0.267,  0.050 ]
])

B = np.array([0.186, -0.126, 0.646, 0.0086])

print("Матрица системы A:")
print(A)
print("\nВектор правых частей B:")
print(B)

detA = np.linalg.det(A)
print(f"\nОпределитель матрицы A = {detA}")

if abs(detA) < 1e-15:
    print("\nТак как определитель равен (или очень близок) нулю, система не имеет единственного решения.")
else:
    print("\nОпределитель A ненулевой, система имеет единственное решение.")

n = A.shape[0]
x = np.zeros(n)

print("\nОпределители A_i (при подстановке столбца B):")
for i in range(n):
    A_i = A.copy()
    A_i[:, i] = B  # Подставляем B в i-й столбец
    detA_i = np.linalg.det(A_i)
    x[i] = detA_i / detA  # Формула Крамера
    print(f"det(A_{i+1}) = {detA_i}")

print("\nРешение системы по Крамеру (x1, x2, x3, x4):")
print(x)